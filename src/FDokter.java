    private void load_data(){
        Object header[]={
            "ID DOKTER", "NAMA", "JENIS KELAMIN", "NO TELP",
            "ALAMAT"
        };
        DefaultTableModel data = new DefaultTableModel(null, header);
        tblDokter.setModel(data);
        String sql = "SELECT * FROM tbl_dokter";
        try {
            st = koneksi.con.createStatement();
            rs = st.executeQuery(sql);
            int no = 1;
            no++;
            while (rs.next()) {                
                String k1 = rs.getString("id_dokter");
                String k2 = rs.getString("nama");
                String k3 = rs.getString("jk");
                String k4 = rs.getString("nope");
                String k5 = rs.getString("alamat");
                String k[] = {k1,k2,k3,k4,k5};
                data.addRow(k);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void input_data()
    {
        try{
            //Mengambil Data Jenis Kelamin
            String jk="";
            if(jkL.isSelected()){jk=jkL.getText();}
            else{jk=jkP.getText();} 
            //Mengambil ComboBox
            st=koneksi.con.createStatement();
          
            //Query Input ke Tabel Dokter
            String sql="INSERT INTO tbl_dokter values('"+id_dokter.getText()
                    +"','"+nama.getText()
                    +"','"+jk
                    +"','"+nope.getText()
                    +"','"+alamat.getText()
                    +"')";
            st.execute(sql);
            JOptionPane.showMessageDialog(null, "Data Dokter Berhasil Dimasukkan");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Terjadi Error Saat Memasukan Data");
        }
    }
    private void Edit(){
        try{
            //Mengambil ComboBox
            st=koneksi.con.createStatement();
            String jk="";
            if (jkL.isSelected()) {
                jk = jkL.getText();
            }else{
                jk = jkP.getText();
            }  
            String sql_edit = "UPDATE tbl_dokter SET "
                    + "id_dokter ='"+id_dokter.getText()
            +"',nama='"+nama.getText()
            +"',jk='"+jk
            +"',nope='"+nope.getText()
            +"',alamat='"+alamat.getText()
            +"'WHERE id_dokter='"+id_dokter.getText()+"'";
                
            st.executeUpdate(sql_edit);
            JOptionPane.showMessageDialog(null, "Data Berhasil DI Update");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void Hapus(){
        try{
            st = koneksi.con.createStatement();
            String sql_delete = "DELETE FROM tbl_dokter "
                    + "WHERE id_dokter='"+id_dokter.getText()+"'";
            st.execute(sql_delete);
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
