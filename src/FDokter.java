    private void tblDokterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDokterMouseClicked
        // TODO add your handling code here:
        int bar = tblDokter.getSelectedRow();
        // TODO add your handling code here:
        String a = tblDokter.getValueAt(bar, 0).toString();
        String b = tblDokter.getValueAt(bar, 1).toString();
        String c = tblDokter.getValueAt(bar, 2).toString();
        String d = tblDokter.getValueAt(bar, 3).toString();
        String e = tblDokter.getValueAt(bar, 4).toString();
        
        id_dokter.setText(a);
        nama.setText(b);
        nope.setText(d);
        if ("L".equals(c)) {
            jkL.setSelected(true);
        }else{
            jkP.setSelected(true);
        }
        alamat.setText(e);
        //set disable button
        id_dokter.setEditable(false);
        btnSimpan.setEnabled(false);
        btnEdit.setEnabled(true);
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tblDokterMouseClicked

