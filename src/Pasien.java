    private void load_data(){
        Object header[]={
            "ID PASIEN", "NAMA", "JENIS KELAMIN","TANGGAL LAHIR", "NO TELP",
            "ALAMAT"
        };
        DefaultTableModel data = new DefaultTableModel(null, header);
        pasien.setModel(data);
        String sql = "SELECT * FROM tbl_pasien";
        try {
            st = koneksi.con.createStatement();
            rs = st.executeQuery(sql);
            int no = 1;
            no++;
            while (rs.next()) {                
                String k1 = rs.getString("id_pasien");
                String k2 = rs.getString("nama");
                String k3 = rs.getString("jk");
                String k4 = rs.getString("tgl_lahir");
                String k5 = rs.getString("nope");
                String k6 = rs.getString("alamat");
                String k[] = {k1,k2,k3,k4,k5,k6};
                data.addRow(k);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void input_data()
    {
        try{
            st=koneksi.con.createStatement();
            //Mengambil Data Jenis Kelamin
            String jk="";
            if(jkL.isSelected()){jk=jkL.getText();}
            else{jk=jkP.getText();} 
            String tampilan = "yyyy-MM-dd";
            SimpleDateFormat fm=new SimpleDateFormat(tampilan);
            String tanggal=String.valueOf(fm.format(tgl_lahir.getDate()));
            String sql="INSERT INTO tbl_pasien values('"+id_pasien.getText()
                    +"','"+nama.getText()
                    +"','"+jk
                    +"','"+tanggal
                    +"','"+nope.getText()
                    +"','"+alamat.getText()
                    +"')";
            st.execute(sql);
            JOptionPane.showMessageDialog(null, "Data Pasien Berhasil Dimasukkan");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void Edit(){
        try{
            st=koneksi.con.createStatement();
            String jk="";
            if (jkL.isSelected()) {
                jk = jkL.getText();
            }else{
                jk = jkP.getText();
            }  
            String tampilan = "yyyy-MM-dd";
            SimpleDateFormat fm=new SimpleDateFormat(tampilan);
            String tanggal=String.valueOf(fm.format(tgl_lahir.getDate()));
            String sql_edit = "UPDATE tbl_pasien SET "
                    + "id_pasien ='"+id_pasien.getText()
            +"',nama='"+nama.getText()
            +"',jk='"+jk
            +"',tgl_lahir='"+tanggal
            +"',nope='"+nope.getText()
            +"',alamat='"+alamat.getText()
            +"'WHERE id_pasien='"+id_pasien.getText()+"'";
            st.executeUpdate(sql_edit);
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Update");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void Hapus(){
        try{
            st = koneksi.con.createStatement();
            String sql_delete = "DELETE FROM tbl_pasien "
                    + "WHERE id_pasien='"+id_pasien.getText()+"'";
            st.execute(sql_delete);
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Hapus");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

