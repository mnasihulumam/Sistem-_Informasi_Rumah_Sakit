        String b = pasien.getValueAt(bar, 1).toString();
        String c = pasien.getValueAt(bar, 2).toString();
        String d = pasien.getValueAt(bar, 3).toString();
        String e = pasien.getValueAt(bar, 4).toString();
        String f = pasien.getValueAt(bar, 5).toString();
        id_pasien.setText(a);
        nama.setText(b);
        nope.setText(e);
